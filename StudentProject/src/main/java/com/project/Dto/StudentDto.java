package com.project.Dto;

/*StudentDto is used to Aggregate Data from Student Entity*/
public class StudentDto {
    public int id;

    public String name;

    public int marks;

    public StudentDto() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMarks() {
        return marks;
    }

    public void setMarks(int marks) {
        this.marks = marks;
    }
}
